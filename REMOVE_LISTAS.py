#Eliminar cierto elementos de una lista predeterminada con la instruccion remove

print("ACTIVIDAD:")
print("Lista: rojo, azul, verde, amarillo, marron, lila, negro, rosa, blanco, naranja")
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']
#Aqui con la intruccion de REMOVE vamos  a ir elminando posiciones y sus elementos que esten
print("\nVamos a eliminar los colores: amarillo y rojo")
colores.remove('amarillo')
colores.remove('rojo')
print("\nAsi queda la nueva lista:")
print(colores)
